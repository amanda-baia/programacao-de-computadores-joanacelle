n1 = input ("Insira o primeiro número")
n2 = input ("Insira o segundo número")

if n1 > n2:
    print ("O primeiro número é o maior entre os dois números")
elif (n1 == n2):
    print ("O valor do primeiro número é igual ao segundo")
